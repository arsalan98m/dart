// ### EXERICES: String interpolation ### //

// using only one variable define like this:

// String title = 'Dart Course';

// write a program that uses `title` to produce the following output:
// Dart Course
// DART COURSE
// dart course

void main() {
  String title = 'Dart Course';
  print(title);

  title = title.toUpperCase();
  print(title);

  title = title.toLowerCase();
  print(title);
}
